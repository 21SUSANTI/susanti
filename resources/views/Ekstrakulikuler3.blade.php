    <p> Ada begitu banyak jenis tarian mulai dari tari tradisional Indonesia, balet, atau modern.<br>
      Menari dapat membantu siswa dalam berkembang secara fisik dan mental karena sebagian besar bentuk tarian membutuhkan disiplin dan tubuh yang kuat.<br>
